import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:joylab/meme/list_meme.dart';

var informationTextStyle = const TextStyle(fontFamily: 'Oxygen');

class MemeDetailScreen extends StatelessWidget {
  final Meme meme;

  const MemeDetailScreen({Key? key, required this.meme}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return LayoutBuilder(
      builder: (BuildContext context, BoxConstraints constraints) {
        if (constraints.maxWidth > 800) {
          return MemeDetailWebPage(meme: meme);
        } else {
          return MemeDetailMobilePage(meme: meme);
        }
      },
    );
  }
}

